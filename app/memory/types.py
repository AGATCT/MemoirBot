WHAT_NOT_TO_SAVE = """
## 什么不应该保存到记忆

- 代码模式、架构、文件路径或项目结构 — 这些可以通过读取当前代码获得
- Git 历史、最近的改动 — `git log` / `git blame` 是权威来源
- 调试方案或修复步骤 — 修复结果已经在代码里，commit message 有上下文
- 已经在 CLAUDE.md 文件中记录的内容
- 临时的任务细节：进行中的工作、临时状态、当前会话上下文
- 即使是用户明确要求记忆的，如果属于以上类别，询问其中 *令人意外* 或 *非显而易见* 的部分是什么 — 那才是值得保存的
"""

